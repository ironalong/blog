<p><strong>Optionally</strong>, when using <code>css3:false</code>, you can add the <a href="http://jqueryui.com/" rel="nofollow">jQuery UI library</a> in case you want to use other easing effects apart from the ones included in the jQuery library (<code>linear</code> and <code>swing</code> ) or the one included by default in fullPage.js (<code>easeInOutCubic</code>).</p>
